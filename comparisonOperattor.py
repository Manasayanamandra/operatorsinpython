'''
| Operator | Meaning          | Example         |
| -------- | ---------------- | --------------- |
| `==`     | Equal to         | `5 == 5 → True` |
| `!=`     | Not equal to     | `5 != 3 → True` |
| `>`      | Greater than     | `4 > 2 → True`  |
| `<`      | Less than        | `3 < 5 → True`  |
| `>=`     | Greater or equal | `5 >= 5 → True` |
| `<=`     | Less or equal    | `3 <= 5 → True` |


 '''
a = 10
b = 20
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)